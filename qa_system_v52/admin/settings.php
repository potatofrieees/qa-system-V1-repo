        $code = $conn->real_escape_string(trim($_POST['area_code']??''));
        $desc = $conn->real_escape_string(trim($_POST['description']??''));
        $sort = (int)($_POST['sort_order']??0);
        if (!$name) { $m='Area name required.'; $t='e'; }
        else {
            $r = $conn->query("INSERT INTO areas (area_code,area_name,description,sort_order) VALUES ('$code','$name','$desc',$sort)");
            $m=$r?'Area added.':'Error: '.$conn->error; $t=$r?'s':'e';
        }
        header("Location: settings.php?tab=areas&msg=".urlencode($m)."&typ=$t"); exit;

    } elseif ($act === 'edit_area') {
        $id   = (int)$_POST['area_id'];
        $name = $conn->real_escape_string(trim($_POST['area_name']??''));
        $code = $conn->real_escape_string(trim($_POST['area_code']??''));
        $desc = $conn->real_escape_string(trim($_POST['description']??''));
        $sort = (int)($_POST['sort_order']??0);
        if (!$name || !$id) { $m='Area name required.'; $t='e'; }
        else {
            $conn->query("UPDATE areas SET area_code='$code',area_name='$name',description='$desc',sort_order=$sort WHERE id=$id");
