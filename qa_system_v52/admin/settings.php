            $m = $ok ? "Test email sent to $test_to. Check your inbox." : 'Failed to send test email. Check SMTP settings and server logs.';
            $t = $ok ? 's' : 'e';
        }
        header("Location: settings.php?tab=mail&msg=".urlencode($m)."&typ=$t"); exit;
    }

    header("Location: settings.php"); exit;
