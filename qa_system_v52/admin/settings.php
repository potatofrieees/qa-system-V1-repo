// Auto-generated by Settings — ".date('Y-m-d H:i:s')."
define('MAIL_ENABLED',    $enabled);
define('SMTP_HOST',       '$host');
define('SMTP_PORT',       $port);
define('SMTP_SECURE',     '$secure');
define('SMTP_AUTH',       $auth);
define('SMTP_USER',       '$user');
define('SMTP_PASS',       '$pass');
define('MAIL_FROM',       '$from');
define('MAIL_FROM_NAME',  '$fname');
