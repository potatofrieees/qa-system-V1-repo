define('APP_URL',         '$app_url');
define('OTP_EXPIRY_MINUTES',    10);
define('OTP_DIGITS',             6);
define('RESET_TOKEN_EXPIRY_MINUTES', 60);
?>\n";

        if (file_put_contents($cfg_file, $new_config)) {
            $m = 'Mail configuration saved.'; $t = 's';
        } else {
            $m = 'Could not write config file. Check file permissions on mail/config.php.'; $t = 'e';
        }
        header("Location: settings.php?tab=mail&msg=".urlencode($m)."&typ=$t"); exit;

    } elseif ($act === 'send_test_mail') {
        $test_to = trim($_POST['test_email']??'');
